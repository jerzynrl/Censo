<p style="font-size: large; text-align: justify;">
<strong style="font-size: x-large;" class="theme_color">CONCEPTOS GENERALES</strong><br><br>
<strong><em>Muestreo:</em></strong><br>Selección de un conjunto de personas o cosas que se consideran representativos del grupo al que pertenecen, con la finalidad de estudiar o determinar las características del grupo.<br><br>

<strong><em>Tamaño de la muestra:</em></strong><br>Es el número de sujetos que componen la muestra extraída de una población necesarios para que los datos obtenidos sean representativos de la población.<br><br>

<strong><em>Muestreo Probabilístico:</em></strong><br>Es una técnica de muestreo en virtud de la cual las muestras son recogidas en un proceso que brinda a todos los individuos de la población las mismas oportunidades de ser seleccionados.<br><br>

<strong><em>Muestreo No Probabilístico:</em></strong><br>El muestreo no probabilístico es una técnica de muestreo donde las muestras se recogen en un proceso que no brinda a todos los individuos de la población iguales oportunidades de ser seleccionados.<br><br>

<strong><em>Distribución de Frecuencia:</em></strong><br>Son tablas en que se dispone las modalidades de la variable por filas. En las columnas se dispone el número de ocurrencias por cada valor, porcentajes, etc.<br><br>

<strong><em>Tabla de Frecuencia:</em></strong><br>Se emplea si las variables toman un número grande de valores o la variable es continua. Se agrupan los valores en intervalos que tengan la misma amplitud denominados clases.<br><br>

<strong><em>Distribución de Clases y Frecuencias:</em></strong><br>Se emplea si las variables toman un número grande de valores o la variable es continua. Se agrupan los valores en intervalos que tengan la misma amplitud denominados clases. A cada clase se le asigna su frecuencia correspondiente.<br><br>

<strong><em>Representación Gráfica:</em></strong><br>Es un tipo de representación de datos, generalmente numéricos, mediante recursos gráficos (líneas, vectores, superficies o símbolos), para que se manifieste visualmente la correlación estadística que guardan entre sí.<br><br>

<strong><em>Gráfica de Variables Continuas:</em></strong><br>Para las variables cuantitativas, consideraremos dos tipos de gráficos, en función de que para realizarlos se usen las frecuencias (absolutas o relativas) o las frecuencias acumuladas: Diagramas diferenciales: Son aquellos en los que se representan frecuencias absolutas o relativas.<br><br>

<strong><em>Gráficas de Variables Discretas:</em></strong><br>Podemos representar los datos en tres gráficos, diagrama de barras, polígono de frecuencias y gráfico de sectores. Diagrama de barras, usado para variables cuantitativas discretas. En el eje X se señalan los valores de la variable y en el eje Y los valores de la frecuencia absoluta.<br><br>

<strong><em>Media:</em></strong><br>Es la suma de todos los datos dividida entre el número total de datos. Se calculan dependiendo de cómo vengan ordenados los datos.<br><br>

<strong><em>Moda:</em></strong><br>La moda de un conjunto de datos es el dato que más veces se repite, es decir, aquel que tiene mayor frecuencia absoluta. Se denota por Mo. En caso de existir dos valores de la variable que tengan la mayor frecuencia absoluta, habría dos modas. Si no se repite ningún valor, no existe moda.<br><br>

<strong><em>Mediana:</em></strong><br>La mediana es el valor que ocupa el lugar central entre todos los valores del conjunto de datos, cuando estos están ordenados en forma creciente o decreciente.<br><br>

<strong><em>Medidas de Posición:</em></strong><br>Como los cuartiles, quintiles, deciles y percentiles dividen a una distribución ordenada en partes iguales. ... El tercer cuartil (Q3) es un valor que supera a lo más al 75 % de los datos y es superado por a lo más el 25 % de ellos.<br><br>

<strong><em>Percentiles:</em></strong><br>Los percentiles son los 99 valores que dividen la serie de datos en 100 partes iguales.<br><br>

<strong><em>Cuartiles:</em></strong><br>Los cuartiles son los tres valores de la variable que dividen a un conjunto de datos ordenados en cuatro partes iguales.<br><br>

<strong><em>Deciles:</em></strong><br>Se refiere a cada uno de los 9 valores que dividen un grupo de datos (clasificados con una relación de orden) en diez partes iguales, y de manera que cada parte representa un décimo de la población. En resumen, los deciles son cada uno de los nueve valores que dividen un conjunto de datos en diez grupos con iguales efectivos. Son los nueve valores que dividen la serie de datos en diez partes.<br><br>
