<strong><em>Distribución Binomial:</em></strong><br>es una distribución de probabilidad discreta que cuenta el número de éxitos en una secuencia de <strong>n</strong> ensayos de Bernoulli independientes entre sí, con una probabilidad fija <strong>p</strong> de ocurrencia del éxito entre los ensayos.<br><br>

<strong><em>Distribución Hipergeométrica:</em></strong><br>Es una distribución discreta relacionada con muestreos aleatorios y sin reemplazo.<br><br>

<strong><em>DDistribución Multibinominal:</em></strong><br>Es una generalización de la distribución binomial. La distribución binomial es la probabilidad de un número de éxitos en N sucesos de Bernoulli independientes, con la misma probabilidad de éxito en cada suceso.<br><br>

<strong><em>Distribución de Poisson:</em></strong><br>Es una distribución de probabilidad discreta que expresa, a partir de una frecuencia de ocurrencia media, la probabilidad que ocurra un determinado número de eventos durante cierto periodo de tiempo.<br><br>

<strong><em>Distribución de Chi Cuadrado:</em></strong><br>Es la distribución muestral de s2. O sea que si se extraen todas las muestras posibles de una población normal y a cada muestra se le calcula su varianza, se obtendrá la distribución muestral de varianzas. Los valores de X2 son mayores o iguales que 0.
