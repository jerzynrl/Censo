<strong><em>Amplitud o Rango:</em></strong><br>Es el intervalo entre el valor máximo y el valor mínimo; por ello, comparte unidades con los datos. Permite obtener una idea de la dispersión de los datos, cuanto mayor es el rango, más dispersos están los datos de un conjunto.<br><br>

<strong><em>Desviación Media:</em></strong><br>La desviación media es una medida de dispersión, una medida de cómo los valores individuales del conjunto pueden diferir de la media. El valor absoluto se usa para evitar que las desviaciones de signo contrario se cancelan mutuamente.<br><br>

<strong><em>Desviación Semi Intercuartil:</em></strong><br>Es un medio de la diferencia entre el primer y tercer cuartiles. Es la mitad de la distancia requerida para cubrir la mitad de las cuentas. El rango semi-intercuartil es afectado muy poco por cuentas extremas. Esto lo hace una buena medida de dispersión para distribuciones sesgadas.<br><br>

<strong><em>Desviación Típica o Estándar:</em></strong><br>La desviación típica o desviación estándar (denotada con el símbolo σ o s, dependiendo de la procedencia del conjunto de datos) es una medida de dispersión para variables de razón (variables cuantitativas o cantidades racionales) y de intervalo. Se define como la raíz cuadrada de la varianza de la variable.<br><br>

